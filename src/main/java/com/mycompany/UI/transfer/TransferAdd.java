    private void SubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitActionPerformed
        frame.setScheduleManagement();
        Team sellerTeam = (Team) teamcb.getSelectedItem(); 
        Player targetPlayer = (Player) playercb.getSelectedItem();
        String feeStr = fee.getText();
        String salaryStr = sal.getText();

        if (sellerTeam == null || targetPlayer == null) {
            JOptionPane.showMessageDialog(this, "Please select team and player!");
            return;
        }
        
        try {
            int feeInt = Integer.parseInt(fee.getText());
            int salaryOffer = Integer.parseInt(sal.getText());
            
            TransferAccess tacc = new TransferAccess(conn);
            boolean success = tacc.sendTransferRequest(
                targetPlayer.id(), 
                getMyTeamId(),    
                sellerTeam.id(),  
                feeInt, 
                salaryOffer
            );
            
            JOptionPane.showMessageDialog(this, "Offer sended!");
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Fee and salary must be number!");
        }
    }//GEN-LAST:event_SubmitActionPerformed


