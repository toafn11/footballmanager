    private void initTeamComboBox() throws SQLException {
        TeamAccess tacc = new TeamAccess(conn);
        List<Team> teams = tacc.listTeam(); 
        
        teams.removeIf(t -> t.id() == getMyTeamId());
        DefaultComboBoxModel<Team> teamModel = new DefaultComboBoxModel<>();
        for (Team t : teams) {
            teamModel.addElement(t);
        }
        teamcb.setModel(teamModel);

        teamcb.addItemListener(e -> {
            if (e.getStateChange() == ItemEvent.SELECTED) {
                Team selectedTeam = (Team) teamcb.getSelectedItem();
                if (selectedTeam != null) {
                    loadPlayersForTeam(selectedTeam.id());
                }
            }
        });

        if (teamModel.getSize() >= 0) {
            teamcb.setSelectedIndex(0);
            Team firstTeam = (Team) teamcb.getSelectedItem();
            loadPlayersForTeam(firstTeam.id());
        }
    }
    private void loadPlayersForTeam(int teamId) {
        TeamAccess tacc = new TeamAccess(conn);
        List<Player> players = tacc.listPlayByTeam(teamId);
        DefaultComboBoxModel<Player> playerModel = new DefaultComboBoxModel<>();
        
        if (players.isEmpty()) {
        } else {
            for (Player p : players) {
                playerModel.addElement(p);
            }
        }
        playercb.setModel(playerModel);
        
        if (!players.isEmpty()) {
            playercb.setSelectedIndex(0);
        }
    }
