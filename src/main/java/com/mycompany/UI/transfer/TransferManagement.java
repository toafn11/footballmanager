    private void acceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_acceptActionPerformed
        int selectedRow = toTable.getSelectedRow();

        int transferId = (int) toTable.getValueAt(selectedRow, 0);
        String state =  toTable.getValueAt(selectedRow, 6).toString();
        if(state.equals("pending")){
            accept.setVisible(true);
            reject.setVisible(true);
        }
        String playerName = toTable.getValueAt(selectedRow, 1).toString();
        String yearsStr = JOptionPane.showInputDialog(this, 
                "Sell " + playerName + "!\nEnter new contract lenght:", 
                "3");

        if (yearsStr == null) return; 

        try {
            int years = Integer.parseInt(yearsStr);
            if (years <= 0) throw new NumberFormatException();
            LocalDate newEndDate = LocalDate.now().plusYears(years);
            TransferAccess tacc = new TransferAccess(conn);
            String msg = tacc.respondToTransfer(transferId, true, newEndDate);
            JOptionPane.showMessageDialog(this, msg);
            addDatatoTable(); 

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Enter a valid year-contract ( 1, 2, 3...)");
        }
    }//GEN-LAST:event_acceptActionPerformed

