    private void rejectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rejectActionPerformed
        int selectedRow = toTable.getSelectedRow();
       
        int transferId = (int) toTable.getValueAt(selectedRow, 0);
        int confirm = JOptionPane.showConfirmDialog(this, 
                "Reject this offer?", "Reject", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            TransferAccess tacc = new TransferAccess(conn);
            String msg = tacc.respondToTransfer(transferId, false, null);

            JOptionPane.showMessageDialog(this, msg);
            addDatatoTable();
        }
    }//GEN-LAST:event_rejectActionPerformed

