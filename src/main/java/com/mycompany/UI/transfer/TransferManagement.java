    public void addDatatoTable(){
        TransferAccess tacc = new TransferAccess(conn);
        List<Transfer> data = new ArrayList<>();
        if(!isAdmin()){
            data = tacc.getPendingTransfers(getMyTeamId());
        } else {
            data = tacc.getAllTransfers();
        }
        DefaultTableModel model = (DefaultTableModel) toTable.getModel();
        model.setRowCount(0);
        for(Transfer t: data){
            model.addRow(new Object[]{
                t.id(),
                t.playerName(),
                t.toTeamName(),
                t.fee(),
                t.salary(),
                t.offerDate(),
                t.state()
                }
            );
        }
    }
    
