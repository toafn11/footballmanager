    public MatchManagement(HomeFrame frame) throws SQLException {
        this.frame = frame;
        initComponents();
        TournamentAccess tacc = new TournamentAccess(conn);
        List<Tournament> tours = tacc.getAllTournaments();
        cbTour.removeAllItems();
        Finalize.setVisible(false);
        nextroundcup.setVisible(false);
        for (Tournament t : tours) {
            cbTour.addItem(t);
        }
        
        cbTour.addItemListener(e -> {
            if (e.getStateChange() == ItemEvent.SELECTED) {
                selectedTour = (Tournament) cbTour.getSelectedItem();
                
                if (selectedTour != null) {
                    try { 
                        addTableData(selectedTour.id());
                        nextroundcup.setVisible(selectedTour.formula().equals("cup"));
                    } catch (SQLException ex) {
                        System.getLogger(TeamCareMain.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
                    }
                }
            }
        });
        if (cbTour.getItemCount() > 0) {
            cbTour.setSelectedIndex(0);
            Tournament first = (Tournament) cbTour.getItemAt(0);
            addTableData(first.id());
        }
    }
    
    private void addTableData(int tourId) throws SQLException {
        TournamentAccess tacc = new TournamentAccess(conn);
        List<Versus> data = tacc.getMatchesByTournamentId(tourId);
        DefaultTableModel model = (DefaultTableModel) matchTable.getModel();
        model.setRowCount(0);

        for(Versus v: data){
            boolean check = v.isDone();
            model.addRow(new Object[]{
                v.matchId(),
                v.tourName(),
                v.homeName(),
                check ? v.homeScore() : "-",
                check ? v.awayScore() : "-",
                v.awayName(),
                v.dt(),
                check ? "Done" : "Not Done"
                }
            );
        }
    }
