    private void FinalizeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FinalizeActionPerformed
        int id = Integer.parseInt(matchTable.getValueAt(matchTable.getSelectedRow(), 0).toString());
        MatchAccess macc = new MatchAccess(conn);
        try {
            Match match = macc.findMatchById(id);
            if(match.isDone()) return;
            
            frame.setMomentAdd(id, match.homeId(), match.awayId());
        } catch (SQLException ex) {
            System.getLogger(MatchManagement.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        }
    }//GEN-LAST:event_FinalizeActionPerformed

    private void Finalize1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Finalize1ActionPerformed
        Tournament currentTour = (Tournament) cbTour.getSelectedItem();
        if (currentTour == null) {
            return;
        }

        this.selectedTour = currentTour;
        try {
            
            nextroundcup.setVisible(currentTour.formula().equals("cup") && isAdmin());
            
            TournamentAccess tacc = new TournamentAccess(conn);
            List<Tournament> tours = tacc.getAllTournaments();
            cbTour.removeAllItems();
            Finalize.setVisible(false);
            nextroundcup.setVisible(false);
            for (Tournament t : tours) {
                cbTour.addItem(t);
            }
            if (cbTour.getItemCount() > 0) {
                cbTour.setSelectedItem(currentTour);
                Tournament first = (Tournament) cbTour.getItemAt(0);
                addTableData(currentTour.id());
            }
            
        } catch (SQLException ex) {
            System.getLogger(MatchManagement.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        }
    }//GEN-LAST:event_Finalize1ActionPerformed

    private void nextroundcupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextroundcupActionPerformed
        int tourId = selectedTour.id();
        TournamentAccess tacc = new TournamentAccess(conn);
        String result = tacc.generateNextCupRound(tourId);
        if (result.contains("Created")) {
            JOptionPane.showMessageDialog(this, result);
            try { 
                addTableData(tourId);
            } catch (SQLException ex) {
                System.getLogger(MatchManagement.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
            }
        } else if (result.contains("Champion")) {
            JOptionPane.showMessageDialog(this, result, "Congratulation!", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, result, "Cannot generate next round", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_nextroundcupActionPerformed


