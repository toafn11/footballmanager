    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        
        PlayerAccess pacc = new PlayerAccess(conn);
        String newpos = posCb.getSelectedItem().toString();
        int newnum = Integer.parseInt(numField.getText());
        
        if(pacc.isNumberTaken(player.id(), newnum)){
            JOptionPane.showMessageDialog(this, 
                    "Number " + newnum + " is already taken in Team!", 
                    "Number Taken", JOptionPane.WARNING_MESSAGE);
                return;
        } 
        if(pacc.updatePlayerInfo(player.id(), newnum, newpos)){
            JOptionPane.showMessageDialog(this, "Player updated!");
        }
        
    }//GEN-LAST:event_saveBtnActionPerformed

