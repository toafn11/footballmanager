    public void addDataTable(){
        PlayerAccess pacc = new PlayerAccess(conn);
        List<PlayerOfTeam> stat = pacc.getPlayerCareerStats(player.id());
        DefaultTableModel data = (DefaultTableModel) statTable.getModel();
        
        for(PlayerOfTeam p : stat){
            data.addRow(new Object[]{
                p.team(),
                p.goal(),
                p.yellowCard(),
                p.redCard(),
                p.contractEnd()
            });
        }
    }
